import JwtToken from '@/components/JwtToken'

test('JwtToken is getting decoded', () => {
  const jwtTokenRaw = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIxMjM0NTY3ODkwIiwibmFtZSI6IkpvaG4gRG9lIiwiaWF0IjoxNTE2MjM5MDIyfQ.SflKxwRJSMeKKF2QT4fwpMeJf36POk6yJV_adQssw5c'
  expect(JwtToken.methods.jwtDecode(jwtTokenRaw).payload.name).toBe('John Doe')
})

test('JwtToken is expired', () => {
  const jwtTokenRaw = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIxMjM0NTY3ODkwIiwibmFtZSI6IkpvaG4gRG9lIiwiaWF0IjoxNTE2MjM5MDIyfQ.SflKxwRJSMeKKF2QT4fwpMeJf36POk6yJV_adQssw5c'
  expect(JwtToken.methods.jsonTokenExpired(jwtTokenRaw)).toBe(true)
})
